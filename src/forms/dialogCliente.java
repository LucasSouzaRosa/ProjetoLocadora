    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed
        // TODO add your handling code here:
        if (textNome.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Favor informar o nome");
            textNome.requestFocus();
            return;
        }
        if (textCpf.getText().equals("   .   .   -  ")){
            JOptionPane.showMessageDialog(null, "Favor informar o CPF");
            textCpf.requestFocus();
            return;
        }
        if (textEndereco.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Favor informar o endereço");
            textEndereco.requestFocus();
            return;
        }
        if (textTelefone.getText().equals("(  )      -    ")){
            JOptionPane.showMessageDialog(null, "Favor informar o telefone");
            textTelefone.requestFocus();
            return;
        }
        if (textEmail.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Favor informar o e-mail");
            textEmail.requestFocus();
            return;
        }
        
        Cliente c1 = new Cliente(textNome.getText(), textEndereco.getText(), textEmail.getText(), textTelefone.getText(), textCpf.getText());
        
        Listas.getListaCliente().add(c1);
        
        textNome.setText("");
        textEmail.setText("");
        textEndereco.setText("");
        textTelefone.setText("");
        textCpf.setText("");
        
        textNome.requestFocus();
        
        textArea.setText("Cliente adicionado");
    }//GEN-LAST:event_btnAdicionarActionPerformed

    private void btnListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnListarActionPerformed
        // TODO add your handling code here:
        if (Listas.getListaCliente().isEmpty()){
            JOptionPane.showMessageDialog(null, "A lista de clientes está vazia");
            textNome.requestFocus();
            return;
        }
        
        textArea.setText("Nome\t\tCPF\t\tEndereco\t\tE-mail\t\tTelefone\n");
        
        for (Cliente c : Listas.getListaCliente()) {
            textArea.append(c.getNome()+"\t\t"+c.getCpf()+"\t\t"+c.getEndereco()+"\t"+c.getEmail()+"\t"+c.getTelefone()+"\n");
        }
    }//GEN-LAST:event_btnListarActionPerformed

